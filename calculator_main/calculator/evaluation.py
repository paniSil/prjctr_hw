def add(a, b):
    result = a + b
    return result


def substract(a, b):
    result = a - b
    return result


def divide (a, b):
    if b != 0:
        result = a / b
        return result
    else:
        print('Zero can\'t be a divider')


def multiply (a, b):
    result = a * b
    return result

